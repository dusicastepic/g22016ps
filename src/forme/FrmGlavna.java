    private void popuniLokacije() {
        jComboBoxlokacije.removeAllItems();
        ArrayList<Lokacija> listaLokacija = Kontroler.getInstance().vratiListuLokacija();
        for (Lokacija lokacija : listaLokacija) {
            jComboBoxlokacije.addItem(lokacija);
        }
    }

    private void popuniRadnike() {
        jComboBoxradnici.removeAllItems();
        ArrayList<Radnik> listaRadnika = Kontroler.getInstance().vratiListuRadnika();
        for (Radnik radnik : listaRadnika) {
            jComboBoxradnici.addItem(radnik);
        }
    }
