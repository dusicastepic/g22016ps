    private void jButtonsacuvajrasporedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsacuvajrasporedeActionPerformed
        //1r 1lokacija
        ModelTabeleRaspored mtr = (ModelTabeleRaspored) jTableRaspored.getModel();
        ArrayList<Raspored> listaRasporeda = mtr.vratiListuRaporeda();
        for (int i = 0; i < listaRasporeda.size(); i++) {
            Raspored r1 = listaRasporeda.get(i);
            for (int j = i + 1; j < listaRasporeda.size(); j++) {
                Raspored r2 = listaRasporeda.get(j);
                if (r1.getRadnik().getRadnikID() == r2.getRadnik().getRadnikID()
                        && r1.getLokacija().getLokacijaID() != r2.getLokacija().getLokacijaID()) {
                    JOptionPane.showMessageDialog(this, "1 radnik moze samo na jednoj lokaciji da radi!!", "Greska", JOptionPane.ERROR_MESSAGE);
                    return;
                }

            }

        }

        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        for (int i = 0; i < listaRasporeda.size(); i++) {
            Raspored r1 = listaRasporeda.get(i);
            String dat1 = sdf.format(r1.getDatum());
            int ukang = r1.getBrojSati();
            for (int j = i + 1; j < listaRasporeda.size(); j++) {
                Raspored r2 = listaRasporeda.get(j);
                String dat2 = sdf.format(r2.getDatum());
                if (r1.getRadnik().getRadnikID() == r2.getRadnik().getRadnikID()
                        && dat1.equals(dat2)) {
                    ukang += r2.getBrojSati();
                }

            }
            if (ukang > 8) {
                JOptionPane.showMessageDialog(this, "1 radnik moze samo 8h da radi dnevno!!", "Greska", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        boolean uspesno=Kontroler.getInstance().sacuvaj(listaRasporeda);
        if (uspesno) {
            JOptionPane.showMessageDialog(this, "Sve uspesno sacuvano!!", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
        JOptionPane.showMessageDialog(this, "Nista nije sacuvano!!", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);}
    }//GEN-LAST:event_jButtonsacuvajrasporedeActionPerformed

