    private void jButtondodajrasporedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtondodajrasporedActionPerformed

        Lokacija lokacija = (Lokacija) jComboBoxlokacije.getSelectedItem();
        Radnik radnik = (Radnik) jComboBoxradnici.getSelectedItem();
        String brsati = null;
        int brojsati = -1;
        try {
            brsati = jTextFieldbrojsati.getText();
            for (int i = 0; i < brsati.length(); i++) {
                if (!Character.isDigit(brsati.charAt(i))) {
                    JOptionPane.showMessageDialog(this, "Broj sati mora biti u ciframa, a ne slovima unet!", "Greska", JOptionPane.ERROR_MESSAGE);
                    return;
                }

            }

            brojsati = Integer.parseInt(brsati);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Broj sati nije unet!", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String dat = null;
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        Date datum = null;
        try {
            dat = jTextFielddatum.getText();
            if (dat.isEmpty()) {
                throw new IllegalArgumentException();
            }
            datum = sdf.parse(dat);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Datum nije unet!", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        } catch (ParseException ex) {

            JOptionPane.showMessageDialog(this, "Nije dobar format datuma unet!");
            return;
        }
        if (brojsati != -1 && datum != null) {
            Raspored r = new Raspored(-1, brojsati, datum, lokacija, radnik);
            ModelTabeleRaspored mtr = (ModelTabeleRaspored) jTableRaspored.getModel();
            mtr.dodaj(r);
        }
    }//GEN-LAST:event_jButtondodajrasporedActionPerformed

